int sum(int i, int j){
    return i + j;
}

int sub(int i, int j){
    return i - j;
}

int mul(int i, int j){
    return i * j;
}

int div(int i, int j){
    return i / j;
}

int mod(int i, int j){
    return i % j;
}

int max1(int i, int j){
    return (i > j ? i : j);
}

int min1(int i, int j){
    return (i < j ? i : j);
}

int square(int i){
    return i * i;
}

int cube(int i){
    return i * i * i;
}
