#include<stdio.h>
#include<math.h>

void main(){
    printf("\n Abs of -5 is %ld", abs(-5));
    printf("\n ceil of 11.5 is %lf", ceil(11.5));
    printf("\n floor of 11.5 is %lf", floor(11.5));
    printf("\n Sqrt of 144 is %lf", sqrt(144));
    printf("\n Power of 2 ^ 10 is %lf", pow(2, 10));
    printf("\n Sin of 0.14 is %lf", sin(0.14));
}
